#Take a user's first name and last name as input and print them in reverse order.

first_name=input("enter the first name:")
print(first_name)
print(first_name[::-1])
last_name=input("enter the last name:")
print(last_name)
print(last_name[::-1])
print(first_name+last_name)
print(first_name[::-1],last_name[::-1])


